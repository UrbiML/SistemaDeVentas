    private void pdf() {
        try {
            int id = vDao.IdVenta();
            FileOutputStream archivo;
            File file = new File("src/pdf/venta" + id + ".pdf");
            archivo = new FileOutputStream(file);
            Document document = new Document();
            PdfWriter.getInstance(document, archivo);
            document.open();

            Image img = Image.getInstance("src/sistemadeventas/img/logo_pdf.png");
            Paragraph fecha = new Paragraph();
            Font negrita = new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.BOLD, BaseColor.BLUE);
            fecha.add(Chunk.NEWLINE);
            Date date = new Date();
            fecha.add("Factura: " + id + "\nFecha: " + new SimpleDateFormat("dd-MM-yyyy").format(date) + "\n\n");

            PdfPTable Encabezado = new PdfPTable(4);
            Encabezado.setWidthPercentage(100);
            Encabezado.getDefaultCell().setBorder(0);
            float[] columnaEncabezado = new float[]{20f, 30f, 70f, 40f};
            Encabezado.setWidths(columnaEncabezado);
            Encabezado.setHorizontalAlignment(Element.ALIGN_LEFT);

            Encabezado.addCell(img);
            String ruc = txtRucConfig.getText();
            String nombre = txtNombreConfig.getText();
            String tel = txtTelefonoConfig.getText();
            String dir = txtDireccionConfig.getText();
            String ra = txtRazonConfig.getText();

            Encabezado.addCell("");
            Encabezado.addCell("Ruc: " + ruc + "\nNombre: " + nombre + "\nTelefno: " + tel + "\nDireccion: " + dir + "\nRazon: " + ra);
            Encabezado.addCell(fecha);
            document.add(Encabezado);

            // Clientes
            Paragraph cli = new Paragraph();
            cli.add(Chunk.NEWLINE);
            cli.add("Datos de los clientes: " + "\n\n");
            document.add(cli);

            PdfPTable tableCli = new PdfPTable(4);
            tableCli.setWidthPercentage(100);
            tableCli.getDefaultCell().setBorder(0);
            float[] columnaCLi = new float[]{20f, 50f, 30f, 40f};
            tableCli.setWidths(columnaCLi);
            tableCli.setHorizontalAlignment(Element.ALIGN_LEFT);
            PdfPCell cl1 = new PdfPCell(new Phrase("Dni/Ruc", negrita));
            PdfPCell cl2 = new PdfPCell(new Phrase("Nombre", negrita));
            PdfPCell cl3 = new PdfPCell(new Phrase("Telefono", negrita));
            PdfPCell cl4 = new PdfPCell(new Phrase("Direccion", negrita));
            cl1.setBorder(0);
            cl2.setBorder(0);
            cl3.setBorder(0);
            cl4.setBorder(0);
            tableCli.addCell(cl1);
            tableCli.addCell(cl2);
            tableCli.addCell(cl3);
            tableCli.addCell(cl4);
            tableCli.addCell(txtDniVenta.getText());
            tableCli.addCell(txtNombreClienteVenta.getText());
            tableCli.addCell(txtTelefonoCVenta.getText());
            tableCli.addCell(txtDirecionCVenta.getText());
            document.add(tableCli);

            // Producto
            PdfPTable tablePro = new PdfPTable(4);
            tablePro.setWidthPercentage(100);
            tablePro.getDefaultCell().setBorder(0);
            float[] columnaPro = new float[]{10f, 50f, 15f, 20f};
            tablePro.setWidths(columnaPro);
            tablePro.setHorizontalAlignment(Element.ALIGN_LEFT);
            PdfPCell pr1 = new PdfPCell(new Phrase("Cantidad", negrita));
            PdfPCell pr2 = new PdfPCell(new Phrase("Descripcion", negrita));
            PdfPCell pr3 = new PdfPCell(new Phrase("Precio U.", negrita));
            PdfPCell pr4 = new PdfPCell(new Phrase("Precio T.", negrita));
            pr1.setBorder(0);
            pr2.setBorder(0);
            pr3.setBorder(0);
            pr4.setBorder(0);
            pr1.setBackgroundColor(BaseColor.DARK_GRAY);
            pr2.setBackgroundColor(BaseColor.DARK_GRAY);
            pr3.setBackgroundColor(BaseColor.DARK_GRAY);
            pr4.setBackgroundColor(BaseColor.DARK_GRAY);
            tablePro.addCell(pr1);
            tablePro.addCell(pr2);
            tablePro.addCell(pr3);
            tablePro.addCell(pr4);

            for (int i = 0; i < tableVenta.getRowCount(); i++) {
                String producto = tableVenta.getValueAt(i, 1).toString();
                String cantidad = tableVenta.getValueAt(i, 2).toString();
                String precio = tableVenta.getValueAt(i, 3).toString();
                String total = tableVenta.getValueAt(i, 4).toString();
                tablePro.addCell(cantidad);
                tablePro.addCell(producto);
                tablePro.addCell(precio);
                tablePro.addCell(total);
            }
            document.add(tablePro);

            Paragraph info = new Paragraph();
            info.add(Chunk.NEWLINE);
            info.add("Total a Pagar: " + totalPagar);
            info.setAlignment(Element.ALIGN_RIGHT);
            document.add(info);

            Paragraph firma = new Paragraph();
            firma.add(Chunk.NEWLINE);
            firma.add("Cancelacion y Firma.\n\n");
            firma.add("------------------------");
            firma.setAlignment(Element.ALIGN_CENTER);
            document.add(firma);

            Paragraph mensaje = new Paragraph();
            mensaje.add(Chunk.NEWLINE);
            mensaje.add("Gracias por su Compra.\n\n");
            mensaje.setAlignment(Element.ALIGN_CENTER);
            document.add(mensaje);

            document.close();
            archivo.close();
            Desktop.getDesktop().open(file);
        } catch (DocumentException | IOException e) {
            System.out.println(e.toString());
        }
    }
